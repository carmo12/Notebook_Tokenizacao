import matplotlib.pyplot as plt
from sklearn.decomposition import PCA


# =========================
# FUNÇÃO: plotar mapa 2D
# =========================
def plotar_mapa(motor, cores_por_label):
    """
    Projeta embeddings em 2D usando PCA e plota um mapa semântico.
    """

    # Reduz dimensionalidade (ex: 384 → 2 dimensões)
    pca = PCA(n_components=2)

    # Aplica PCA nos embeddings
    coords = pca.fit_transform(motor.embeddings)

    # Variância explicada por cada componente
    var1 = pca.explained_variance_ratio_[0]
    var2 = pca.explained_variance_ratio_[1]

    # Cria figura
    fig, ax = plt.subplots(figsize=(11, 7))

    # Percorre cada ponto (texto)
    for i, (x, y) in enumerate(coords):

        # Define cor baseada no label
        cor = cores_por_label.get(motor.labels[i], "#999")

        # Plota ponto no gráfico
        ax.scatter(
            x, y,
            c=cor,
            s=200,
            edgecolors="#333",
            linewidths=0.5,
            zorder=5
        )

        # Adiciona label próximo ao ponto
        ax.annotate(
            motor.labels[i],
            (x, y),
            textcoords="offset points",
            xytext=(0, 10),
            ha='center',
            fontsize=9,
            color=cor
        )

    # Títulos e labels
    ax.set_title("Mapa Semântico - Embeddings 2D", fontsize=14, fontweight="bold")
    ax.set_xlabel(f"PC1 ({var1:.1%} variância)")
    ax.set_ylabel(f"PC2 ({var2:.1%} variância)")

    # Grid para melhor leitura
    ax.grid(True, alpha=0.2)

    # Ajuste layout
    plt.tight_layout()
    plt.show()


