# Biblioteca para embeddings e similaridade
from sentence_transformers import SentenceTransformer
from sklearn.metrics.pairwise import cosine_similarity


# =========================
# MODELO DE EMBEDDING
# =========================
# Modelo leve e eficiente para similaridade semântica
modelo_emb = SentenceTransformer('all-MiniLM-L6-v2')


# =========================
# CLASSE PRINCIPAL
# =========================
class MotorSimilaridade:

    def __init__(self, textos, labels):
        """
        Inicializa o motor com textos e seus rótulos.
        """
        self.textos = textos
        self.labels = labels

        # Gera embeddings vetoriais para todos os textos
        # Cada texto vira um vetor numérico
        self.embeddings = modelo_emb.encode(textos)


    def matriz_similaridade(self):
        """
        Calcula matriz de similaridade entre todos os textos.
        """
        return cosine_similarity(self.embeddings)


    def mais_similar(self, query):
        """
        Encontra o texto mais similar a uma query.
        """

        # Gera embedding da query
        emb_q = modelo_emb.encode([query])

        # Calcula similaridade entre query e todos os textos
        scores = cosine_similarity(emb_q, self.embeddings)[0]

        # Encontra índice do maior score
        idx = scores.argmax()

        # Retorna label, score e texto mais similar
        return self.labels[idx], float(scores[idx]), self.textos[idx]


    def exibir_matriz(self):
        """
        Exibe matriz de similaridade formatada.
        """
        mat = self.matriz_similaridade()

        # Cabeçalho com labels
        header = " " * 10 + " ".join(f"{l:>10}" for l in self.labels)
        print(header)

        # Linhas da matriz
        for i, l in enumerate(self.labels):
            print(f"{l:>10} " + " ".join(f"{mat[i][j]:>10.3f}" for j in range(len(self.labels))))


