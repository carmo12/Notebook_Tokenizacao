from transformers import pipeline
import warnings
warnings.filterwarnings("ignore")

def demonstrar_pipeline(nome, textos, subarea):
    """
    Executa um pipeline de IA (ex.: sentiment-analysis, summarization, etc.)
    e demonstra como cada subárea resolve um tipo diferente de problema.
    """
    print(f"\nPipeline: {nome} | Subárea: {subarea}")
    print("-" * 60)

    # Criar o pipeline desejado
    pipe = pipeline(nome)

    # Executar o pipeline para cada texto
    for texto in textos:
        if not texto:
            continue

        resultado = pipe(texto)

        print(f"\n📌 Input: \"{texto[:60]}...\"")
        print(f"➡️  Output: {resultado}")


