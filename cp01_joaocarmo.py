MEU_PROMPT = "Explique o que é inteligência artificial em 2 frases."

# -------------------------------------------
# Executar o laboratório
# -------------------------------------------
if MEU_PROMPT:
    res = lab_temperatura(MEU_PROMPT)

    for t, d in res.items():
        print(f"\nTemperatura {t} - Consistência: {d['consistencia']:.0f}%")
        for i, r in enumerate(d["respostas"], 1):
            print(f"  Resposta {i}: {r[:100]}...")

# -------------------------------------------
# Gerar gráfico
# -------------------------------------------
temps = list(res.keys())
consist = [res[t]["consistencia"] for t in temps]

plt.figure(figsize=(8, 5))
plt.bar([str(t) for t in temps], consist)
plt.title("Consistência por Temperatura")
plt.xlabel("Temperatura")
plt.ylabel("Consistência (%)")
plt.ylim(0, 100)
plt.tight_layout()
plt.show()

