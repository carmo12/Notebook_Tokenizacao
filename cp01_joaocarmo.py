# =========================================================
# FUNÇÃO: analisar_tokens
# Objetivo: analisar um texto e extrair métricas de tokens
# =========================================================
def analisar_tokens(texto):

    # Converte o texto em tokens (unidades que o modelo entende)
    tokens = encoder.encode(texto)

    return {
        # Retorna apenas os primeiros 60 caracteres (resumo do texto)
        "texto": texto[:60],

        # Quantidade total de caracteres no texto original
        "num_caracteres": len(texto),

        # Quantidade de tokens gerados pelo tokenizador
        "num_tokens": len(tokens),

        # Relação caracteres por token (ajuda a entender eficiência)
        # Quanto maior, melhor (menos tokens para representar o texto)
        "ratio": len(texto) / len(tokens) if len(tokens) > 0 else 0,

        # Decodifica os primeiros 10 tokens para visualização
        # Útil para entender como o texto foi quebrado
        "tokens_decodificados": [encoder.decode([t]) for t in tokens[:10]]
    }


