# DESAFIO 4: Complete o laboratório de temperatura
# Teste 3 temperaturas diferentes e gere um gráfico de consistência

MODELO = "mistral/Mistral-7B-Instruct-v0.3"
client = InferenceClient(token=HF_TOKEN)

# -------------------------------------------
# Função que chama o modelo com temperatura
# -------------------------------------------
def chamar_llm(prompt, temperature=0.7, max_tokens=200):
    """
    Envia um prompt ao LLM com uma temperatura específica.
    Retorna apenas o texto da resposta.
    """
    try:
        resp = client.chat.completions.create(
            model=MODELO,
            messages=[{"role": "user", "content": prompt}],
            temperature=temperature,
            max_tokens=max_tokens
        )
        return resp.choices[0].message.content.strip()

    except Exception as e:
        return f"Erro: {e}"


# ------------------------------------------------------------
# Função para testar várias temperaturas repetidas vezes
# ------------------------------------------------------------
def lab_temperatura(prompt, temps=(0.1, 0.7, 1.5), reps=2):
    """
    Executa o mesmo prompt várias vezes com temperaturas diferentes.
    Calcula:
        - respostas obtidas
        - quantidade de respostas únicas
        - consistência (%)
    """
    resultados = {}

    for temp in temps:
        respostas = []

        # Fazer reps chamadas por temperatura
        for _ in range(reps):
            r = chamar_llm(prompt, temperature=temp)
            respostas.append(r)
            time.sleep(2)  # evita rate-limit

        # respostas únicas
        unicas = len(set([r[:50] for r in respostas]))

        # consistência = quanto as respostas são semelhantes (%)
        consistencia = (1 - (unicas - 1) / max(reps - 1, 1)) * 100

        resultados[temp] = {
            "respostas": respostas,
            "consistencia": consistencia
        }

    return resultados


# -------------------------------------------
